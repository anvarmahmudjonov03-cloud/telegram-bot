export type TKey = keyof typeof dict.en;

export function translate(lang: Lang, key: TKey): string {
  return dict[lang][key] ?? dict.en[key];
}

/** Persisted language hook (defaults to Uzbek) */
export function useLang(): [Lang, (l: Lang) => void, (k: TKey) => string] {
  const [lang, setLangState] = useState<Lang>("uz");
  useEffect(() => {
    const saved = localStorage.getItem("store_lang") as Lang | null;
    if (saved && ["uz", "ru", "en"].includes(saved)) setLangState(saved);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("store_lang", l);
  };
  return [lang, setLang, (k: TKey) => translate(lang, k)];
}

/** Pick the announcement matching the selected language */
export function pickAnnouncement(
  lang: Lang,
  s: { announcement: string; announcementRu: string; announcementEn: string }
): string {
  if (lang === "ru") return s.announcementRu || s.announcement;
  if (lang === "en") return s.announcementEn || s.announcement;
  return s.announcement;
}

/** Localized order-status label */
const STATUS_KEY: Record<string, TKey> = {
  pending: "stPending",
  waiting_payment: "stWaiting",
  paid: "stPaid",
  processing: "stProcessing",
  completed: "stCompleted",
  rejected: "stRejected",
  cancelled: "stCancelled",
};

export function statusLabel(lang: Lang, status: string): string {
  const key = STATUS_KEY[status];
  return key ? translate(lang, key) : status;
}
